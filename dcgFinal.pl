/*========================================================================
   Syntax rules 
========================================================================*/

s(s(NP,VP))-->np(NP),vp(VP).
s(s(PP_NP,VP))-->pp_np(PP_NP),vp(VP).
s(s(NP,VP_PP))-->np(NP),vp_pp(VP_PP).

np(np(D,AP))-->det(D),ap(AP).
np(np(D,N))-->det(D),n(N).
np(np(QUAT,N))-->quat(QUAT),n(N).
np(np(PN))-->pn(PN).

pp_np(pp_np(PP,NP))-->pp(PP),np(NP).
vp_pp(vp_pp(VP,PP))-->vp(VP),pp(PP).
  
vp(vp(VT,NP))-->vt(VT),np(NP).
vp(vp(VI))-->vi(VI).
vp(vp(VD,NP))-->vd(VD),np(NP).

ap(ap(ADJ,N))-->adj(ADJ),n(N).
pp(pp(ON,DAY))-->on(ON),day(DAY).  

vt(vt(PRES,V))-->pres(PRES),vt_pres(V).
vt(vt(FUT,V))-->fut(FUT),vt_fut(V).
vi(vi(PRES,V))-->pres(PRES),vi_pres(V).
vd(vd_past(DTV_PAST,NP))-->dtv_past(DTV_PAST),np(NP).
vd(vd_fut(FUT,VD))-->fut(FUT),vp_vd(VD).
vp_vd(vp_vd(DTV,NP))-->dtv(DTV),np(NP).


